#ifndef _FAST_ATAN2_
#define _FAST_ATAN2_
float fast_atan2(float y, float x){
	float phi[] = {-98,  -110,  -126,  -144,  -160,  -174,
				   172,   160,   144,   126,   110,    96,
				   -84,   -70,   -54,   -36,   -20,    -8,
					 6,    20,    36,    54,    70,    82};
	int idx = 0;
	idx -= 12*((-x>0) - 1);
	
	idx -= ( (-y/2>0) - 1);
	
	idx -= ((-(-x)+2*(-y/2) > 0) - 1);
	
	idx -= ( (-x + 2*(-y/2) > 0) - 1);
	
	idx -= ( (-2*(-x) + (-y/2) > 0) - 1);
    
	idx -= ((-(-x)+8*(-y/2)>0) - 1);
	
	idx -= ( (-x + 8*(-y/2)> 0) - 1);
	
	idx -= ( (2*(-x)+(-y/2)> 0)  - 1);
	
	idx -= ( (-x + (-y/2)> 0) - 1);
	
	idx -= ( (-(-x) +(-y/2) > 0) - 1);
	
	idx -= ( (-x + 4*(-y/2) > 0) - 1);
	
	idx -= ( (-(-x)+4*(-y/2)> 0) - 1);
	
	return 3.1419/180 * phi[idx];
	
}

#endif // _FAST_ATAN2_
